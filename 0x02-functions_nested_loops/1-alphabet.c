#include "main.h"
#include <stdio.h>
/**
 * main - function that prints the alphabet, in lowercase.
 * discription - using putchar twice.
 * Return - Always 0 (success).
 */
void print_alphabet(void)
{
	/* to print alphabets in lowercase */
	for (char c = 'a'; c <= 'z'; c++)
	{
		putchar(c);
	}
	putchar('\n');
}
int main()
{
	void print_alphabet(void);
	return (0);
}
